"""Render a `testintro diff --json` result into a sticky PR-comment markdown body.

Used by the Stage-2 comment workflow:
    python -m tools.testing.introspection.render_comment < diff.json > comment.md

Reads the diff result (the object printed by `testintro diff --json`) from stdin or a
file argument and prints markdown. Tests are grouped by the set of platforms they were
added/removed on (the same grouping the CLI uses), collapsed in <details>, with a hard
size cap so the comment stays under GitHub's ~65k limit.
"""

from __future__ import annotations

import hashlib
import json
import os
import sys
from pathlib import Path
from typing import TYPE_CHECKING

from tools.testing.introspection import diff as diff_mod


if TYPE_CHECKING:
    from collections.abc import Callable


MARKER = "<!-- testintro-diff -->"
_MAX_PER_SECTION = 300  # test lines per added/removed section before truncating
_MAX_CHARS = 60000  # stay under GitHub's ~65k comment limit


def _short(ref: str) -> str:
    return (
        ref[:7] if len(ref) == 40 and all(c in "0123456789abcdef" for c in ref) else ref
    )


def _plats(fs: frozenset[str], all_jobs: set[str]) -> str:
    return "all platforms" if set(fs) == all_jobs else ", ".join(sorted(fs))


def _linker(
    loc_map: dict, repo: str, pr: int | None, sha: str, side: str
) -> Callable[[str], str | None]:
    """Build test-id -> source URL from a {test_id: [file, line]} map (None when the
    test has no recorded location). With a PR number, link into the PR's diff view --
    the file anchor is sha256 of the path, side 'R' = added (new) line, 'L' = removed
    (old) line. Without a PR (e.g. CLI --json), fall back to a blob link at `sha`."""

    def link(test_id: str) -> str | None:
        loc = loc_map.get(test_id)
        if not loc:
            return None
        path, line = loc
        if pr:
            anchor = "diff-" + hashlib.sha256(path.encode()).hexdigest()
            return f"https://github.com/{repo}/pull/{pr}/files#{anchor}{side}{line}"
        return f"https://github.com/{repo}/blob/{sha}/{path}#L{line}"

    return link


def _section(
    title: str,
    m: dict[str, set[str]],
    all_jobs: set[str],
    link: Callable[[str], str | None],
) -> list[str]:
    if not m:
        return []
    lines = [f"<details>\n<summary>{title} ({len(m)})</summary>\n"]
    shown = 0
    for fs, tests in diff_mod.group_by_platform_set(m, all_jobs):
        lines.append(f"\n**{_plats(fs, all_jobs)}**")
        for t in tests:
            if shown >= _MAX_PER_SECTION:
                break
            url = link(t)
            lines.append(f"- [`{t}`]({url})" if url else f"- `{t}`")
            shown += 1
        if shown >= _MAX_PER_SECTION:
            lines.append(f"\n_... {len(m) - shown} more (see the CI job artifact)_")
            break
    lines.append("\n</details>")
    return lines


def _fmt_duration(elapsed_s: float | None) -> str | None:
    if elapsed_s is None:
        return None
    s = int(round(elapsed_s))
    return f"{s // 60}m {s % 60:02d}s" if s >= 60 else f"{s}s"


def _platforms_block(job_names: list[str]) -> list[str]:
    """A dropdown listing every platform/config the diff was computed against (the
    `per_job` keys)."""
    lines = [f"<details>\n<summary>Platforms considered ({len(job_names)})</summary>\n"]
    lines += [f"- `{j}`" for j in sorted(job_names)]
    lines.append("\n</details>")
    return lines


def _footer(elapsed_s: float | None) -> str:
    dur = _fmt_duration(elapsed_s)
    return f"_Generated by testintro in {dur}._" if dur else "_Generated by testintro._"


def render(res: dict) -> str:
    added, removed, job_names = diff_mod.invert_per_job(res)
    all_jobs = set(job_names)
    header = (
        f"{MARKER}\n"
        f"### Test Change Dashboard\n\n"
        f"`{_short(res['from'])}..{_short(res['to'])}` across {len(job_names)} platforms "
        f"— **+{len(added)} added, −{len(removed)} removed**"
    )
    if not added and not removed:
        parts = [header, "", "No tests added or removed.", ""]
        parts += _platforms_block(job_names)
        parts += ["", _footer(res.get("elapsed_s"))]
        return "\n".join(parts)

    parts = [header, ""]
    if res.get("broad"):
        parts += [
            "> ⚠️ This PR changes the test-generation surface (e.g. `common_*` / op_db "
            "/ codegen), which can change tests anywhere; comparison is limited to "
            "`linux-cpu`.",
            "",
        ]
    repo = os.environ.get("GITHUB_REPOSITORY", "pytorch/pytorch")
    pr = res.get("pr")
    add_link = _linker(res.get("added_loc", {}), repo, pr, res["to"], "R")
    rem_link = _linker(res.get("removed_loc", {}), repo, pr, res["from"], "L")
    parts += _section("➕ Added", added, all_jobs, add_link)
    parts += _section("➖ Removed", removed, all_jobs, rem_link)
    parts += _platforms_block(job_names)

    # Footnotes: how much was compared (scope is identical across jobs) + any files
    # that couldn't be collected at one of the refs. The broad case is already
    # explained by the warning above, so only the scoped case needs a note here.
    per_job = res.get("per_job", {})
    uncomparable = sorted(
        {f for jr in per_job.values() for f in jr.get("uncomparable", [])}
    )
    is_broad = res.get("broad") or any(
        "broad" in (jr.get("scope_reason") or "") for jr in per_job.values()
    )
    notes = []
    if not is_broad:
        notes.append(
            "compared only the changed test files and the tests that import them"
        )
    if uncomparable:
        notes.append(
            f"{len(uncomparable)} file(s) could not be compared (collection error at a ref)"
        )
    if notes:
        parts += ["", "> " + " · ".join(notes)]
    parts += ["", _footer(res.get("elapsed_s"))]

    body = "\n".join(parts)
    if len(body) > _MAX_CHARS:
        body = (
            body[:_MAX_CHARS]
            + "\n\n_(truncated; see the CI job artifact for the full list)_"
        )
    return body


def main(argv: list[str] | None = None) -> int:
    argv = sys.argv[1:] if argv is None else argv
    text = Path(argv[0]).read_text() if argv else sys.stdin.read()
    print(render(json.loads(text)))
    return 0


if __name__ == "__main__":
    sys.exit(main())
